package exemplo.loop;

public class ExemploWhile {

	public static void main(String[] args) {
		// criando uma vareavel para construcao do loop while
		int num = 0;
		
		//construindo o loop
		while (num < 10) {
			System.out.println("O valor do num é: " + num);
			//bloco que sera execultado enquanto a condicao for verdadeira
			num++;
			
		}
	}

}
